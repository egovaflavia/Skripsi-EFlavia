 <h2 align="justify">Toko Istana Karpet dan Perabot adalah sebuah Toko yang menyediakan berbagai macam kebutuhan rumah tangga seperti karpet dan furniture yang berada di Padang Aro Nagari Lubuk Gadang Kecamatan Sangir Kabupaten Solok Selatan.</h2> 
